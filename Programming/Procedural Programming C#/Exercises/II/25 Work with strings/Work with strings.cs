            string str3 = "СЖВ - парадоксальная помесь фошыстов с толерастами";
            string str4 = "Котики";

            Console.WriteLine(IsPossible(str3, str4));

        }
        static bool IsPossible(string sample, string someString)
        {
            sample = sample.ToLower();
            someString = someString.ToLower();
            int switcher = 0;
            for (int i = 0; i < someString.Length; i++)
            {
                if (someString[i] == ' ') continue;

                for (int j = 0; j < sample.Length; j++)
                {
                    if (someString[i] == sample[j]) { sample = sample.Remove(j, 1); switcher++; break; }
                }
                if (switcher == 0) return false;
                switcher = 0;
            }
            return true;
        }
