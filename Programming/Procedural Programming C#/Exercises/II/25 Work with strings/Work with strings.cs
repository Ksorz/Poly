        static void Main(string[] args)
        {
            string str1 = "Как известно, существует несколько подходов в обработке строк. Это задание нужно сделать используя функции класса string." +
                " необходимо предварительно изучить материал доступный по ссылкам в этом блоке.";
            string str2 = "Елена Малышева: Вы знаете, произошло сногсшибательное событие. Вышли специальные международные рекомендации, которые в " +
                "переводе на русский язык звучат так: «Как правильно какать». Это связано с тем, что у огромного количества людей запоры, грыжи, " +
